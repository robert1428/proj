package io;

public class InregistrariObj {

	public final int id;
	public final String sEmail;
	public final String sAutori;
	public final String sTitlu;
	public final String sAffiliation;

	public InregistrariObj(final int id, final String sEmail, final String sAutori, final String sTitlu,
			final String sAffiliation) {
		this.id = id;
		this.sEmail = sEmail;
		this.sAutori = sAutori;
		this.sTitlu = sTitlu;
		this.sAffiliation = sAffiliation;

	}
}
